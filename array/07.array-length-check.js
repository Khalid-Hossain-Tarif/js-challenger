// Count number of elements in JavaScript array
// Write a function that takes an array (a) as argument. Return the number of elements in a.

const arrayLengthChecker = (a) => {
    return a.length;
}

console.log(arrayLengthChecker([1,2,2,4])); //4
console.log(arrayLengthChecker([9,9,9])); //3
console.log(arrayLengthChecker([4,3,2,1,0])); //5